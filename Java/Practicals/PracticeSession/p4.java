class C2W_ShortDemo{
	public static void main(String[] args){
		short sh1 = 'A';	/*As integer and char types are closely related and 
					also we know that smaller type can be converted to a higher size,
					thus character gets evaluated to its integer ascii code. */
		short sh2 = '1';
		System.out.println(sh1);
		System.out.println(sh2);
	}
}
