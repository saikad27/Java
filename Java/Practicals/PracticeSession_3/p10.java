class p10 {
    public static void main(String[] args) {
        System.out.println("Using while loop:");
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }

        System.out.println("\nUsing for loop:");
        for (int j = 1; j <= 5; j++) {
            System.out.println(j);
        }
    }
}

