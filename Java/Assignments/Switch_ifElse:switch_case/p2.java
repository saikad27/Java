class p2{

	public static void main(String[] args){

		char grade = 'A';
		switch(grade){
			
			case 'O':
				System.out.println("Outstanding");
				break;
			case 'A':
				System.out.println("Excellent");
				break;
			case 'B':
				System.out.println("Good");
				break;
			case 'C':
				System.out.println("Average");
				break;
			case 'D':
				System.out.println("Below Average");
				break;
			case 'F':
				System.out.println("Fail");
				break;
		}
	}
}
