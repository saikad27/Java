//Assignment operators : =,+=,-=,*=,/=,%=
class AssignmentOperators{
	public static void main(String[] args){
		int x = 10;
		int y = 4;
		System.out.println("x : "+(x));
		System.out.println("y : "+(y));
		System.out.println("x+=y : "+(x+=y));
		System.out.println("x-=y : "+(x-=y));
		System.out.println("x*=y : "+(x*=y));
		System.out.println("x/=y : "+(x/=y));
		System.out.println("x%=y : "+(x%=y));
	}
}
