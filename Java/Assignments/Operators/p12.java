class Operators{
	public static void main(String[] args){
		int a = 10;
		int b = 7;
		System.out.println(a%b);	//Arithmetic Operator //3
		System.out.println(a%=b);	//Assignment Operator //3
		System.out.println(a--);	//Unary Operator      //3
		System.out.println(a>--a);	//Relational Operator	//true
		System.out.println(true && false);	//Logical Operator //false
		System.out.println(a^b);	//Bitwise Operator	//0110 = 6
	}
}
